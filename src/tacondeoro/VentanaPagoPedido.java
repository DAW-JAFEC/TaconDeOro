    private void bt_realizarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_realizarPedidoActionPerformed
        // TODO add your handling code here:
        Date fecha = new Date();
        ArrayList<LineaPedido> lineasDePedido = new ArrayList<>();
        for (int i = 0; i < dlmCestaDefinitiva.getSize(); i++) {
            lineasDePedido.add(i,(LineaPedido) dlmCestaDefinitiva.getElementAt(i));
        }

        ArrayList<Ruta> rutas = Ruta.obtenerRutas();
        int numRuta = Ruta.obtenerIdRutaSocio(rutas, usuario);
        DatabaseConnection db = new DatabaseConnection();
        Pedido pedido = new Pedido(fecha, Float.parseFloat(tf_total.getText()), lineasDePedido, usuario.getIdSocio(), numRuta);

        if (dlmTarjetas.size() == 0) {
            JOptionPane.showMessageDialog(this, "No puedes realizar el pedido si no tienes una tarjeta con la que realizar el pago");
        } else {
            if (lst_TarjetasDisponibles.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this, "Debes de seleccionar una tarjeta con la que realizar el pago");
            } else {
                if (numRuta == 0) {
                    JOptionPane.showMessageDialog(this, "Lo sentimos, nuestra empresa no podrá repartir tu pedido por temas de transporte");
                } else {
                    db.aniadirPedidoBBDD(pedido);
                    pedido.setIdPedido(Pedido.obtenerUltimoPedido());
                    LineaPedido.aniadirLineasDelPedido(pedido);
                }
            }
        }
    }//GEN-LAST:event_bt_realizarPedidoActionPerformed

