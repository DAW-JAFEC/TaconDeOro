    public VentanaPagoPedido(java.awt.Frame parent, boolean modal, Socio socio) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);
        padre = (VentanaClientes) parent;
        dlmCestaDefinitiva = new DefaultListModel();
        dlmTarjetas = new DefaultListModel();
        lst_CestaDefinitiva.setModel(dlmCestaDefinitiva);
        lst_TarjetasDisponibles.setModel(dlmTarjetas);
        usuario = socio;
        tf_socioCliente.setText(usuario.getNombre());

        for (int i = 0; i < padre.dlmLineasPedido.size(); i++) {
            LineaPedido lp = (LineaPedido) padre.dlmLineasPedido.getElementAt(i);
            dlmCestaDefinitiva.addElement(lp);
        }

        float total = 0;
        for (int i = 0; i < dlmCestaDefinitiva.size(); i++) {
            LineaPedido lp = (LineaPedido) dlmCestaDefinitiva.getElementAt(i);
            total = total + (lp.getArticuloLinea().getPrecio() * lp.getCantidad());
        }
        tf_total.setText(String.valueOf(total));
        tf_total.setEditable(false);

        ArrayList<TarjetaBancaria> atb = TarjetaBancaria.consultarTarjetasUsuario(usuario);
        if (!atb.isEmpty()) {
            dlmTarjetas.addAll(atb);
        } else {
            JOptionPane.showMessageDialog(this, "Aún no dispone de ninguna tarjeta bancaria asociada a su cuenta");
        }

        lst_CestaDefinitiva.updateUI();
        lst_TarjetasDisponibles.updateUI();
    }

